from django.apps import AppConfig


class ApiConfig(AppConfig):
    """Конфигурация приложения api."""

    # Тип поля ID по умолчанию для моделей этого приложения
    default_auto_field = 'django.db.models.BigAutoField'

    # Имя приложения — должно совпадать с именем папки
    name = 'api'

    # Человекочитаемое название (показывается в админке)
    verbose_name = 'API Автосервис'

    def ready(self):
        """
        Вызывается один раз при запуске Django-сервера.
        Здесь импортируем сигналы — это их «активирует».
        
        Без этого импорта @receiver-декораторы в signals.py
        не будут присоединены к событиям моделей.
        """
        import api.signals  # noqa — импорт нужен для регистрации сигналов
